# Classes

# Classes são paradgmas de programação
# para representar  coisas do mundo real em objeto


# Ou de maneira mais ludica
# Classe é uma maneira de criar objetos
# As classes em Python são elementos que ajudam a otimizar a leitura dos códigos.
#
# Elas servem, sobretudo, para agrupar as funções de um determinado objeto dentro da programação de um software.


# O que são objetos?

# objeto pode-se dizer que é uma instancia de uma classe
# ou seja quando temos um objeto temos uma instancia a partir de uma classe

# Como se define uma classe?

# Uma classe é definida pela palavra reservada class

# Quando queremos criar uma classe devemos colocar a palavra class e logo em seguida o nome da classe

#Exemplo:

class Pessoa:
    pass
    # Isto é uma classe

#Agora vamos criar um objeto dessa classe

classe = Pessoa()

# "classe" é o objeto da classe pessoa

# Podemos ter varias instancias da mesma classe

ana = Pessoa()
paulo = Pessoa()

abacaxi = Pessoa()

# Cada objeto desse criado vai armazenar seu proprio valor.
